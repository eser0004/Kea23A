package JavaDataTypes;

public class Booleans {
    public static void main(String[] args) {

        /*
        In programming you will sometimes be in need of a datatype that only has one of two values, like:
        - Yes / No
        - On / OFF
        - True / False
        For this, in Java we use "boolean", which can only take the values : true or false
         */

        boolean isEserKurdish = true;
        boolean isRottenFishTasty = false;
        System.out.println(isEserKurdish); // outputs true
        System.out.println(isRottenFishTasty); // outputs false

        // Booleans are mostly used for conditional testing
    }
}
